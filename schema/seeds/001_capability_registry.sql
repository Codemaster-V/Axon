--   Formalise the S2 capability_registry seed data in a dedicated seed file.
--
-- Relationship to migration:
--   S2 currently also seeds these rows in schema/migrations/001_initial_schema.sql
--   so the capability hard gate is immediately usable after migration.
--   This file does not replace or modify that migration. It is additive and
--   safe to run repeatedly.
--
-- Idempotency:
