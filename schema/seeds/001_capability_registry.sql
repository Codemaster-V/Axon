--   notes, or implementation statuses change later. Changes to seeded
--   capability definitions should be handled by a future explicit migration
--   or controlled upsert.
--
-- Safety:
--   Both permitted and explicitly blocked MVP action classes are registered.
--   Unknown or absent capabilities must fail closed in application code.

